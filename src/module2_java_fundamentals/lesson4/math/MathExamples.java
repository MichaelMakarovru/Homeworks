package module2_java_fundamentals.lesson4.math;

public class MathExamples {

    public static void main(String[] args) {

        System.out.println("Round 2.3456 = " + Math.round(2.3456));
        System.out.println("Floor 2.3456 = " + Math.floor(2.3456));
        System.out.println("Ceil 2.356 = " + Math.ceil(2.356));
        System.out.println("ABS -5 = " + Math.abs(-5));
        System.out.println("Max 5 & 2 = " + Math.max(5, 2));
        System.out.println("Min 5 & 2 = " + Math.min(5, 2));
        System.out.println("5 in power of 2 = " + Math.pow(5, 2));
        System.out.println("5.2 sin = " + Math.sin(5.2));
        System.out.println("5.2 cos = " + Math.cos(5.2));
        System.out.println("5.2 tan = " + Math.tan(5.2));
        System.out.println("PI = " + Math.PI);
    }
}
